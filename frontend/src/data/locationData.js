// Helper function to get all states
export const getStates = () => {
  return Object.keys(locationData).sort();
};

// Helper function to get districts by state
export const getDistricts = (state) => {
  if (!state || !locationData[state]) return [];
  return Object.keys(locationData[state].districts).sort();
};

// Helper function to get tahsils by state and district
export const getTahsils = (state, district) => {
  if (!state || !district || !locationData[state] || !locationData[state].districts[district]) return [];
  return Object.keys(locationData[state].districts[district].tahsils).sort();
};

// Helper function to get PIN codes by state, district, and tahsil
export const getPinCodes = (state, district, tahsil) => {
  if (!state || !district || !tahsil || 
      !locationData[state] || 
      !locationData[state].districts[district] || 
      !locationData[state].districts[district].tahsils[tahsil]) {
    return [];
  }
  return locationData[state].districts[district].tahsils[tahsil].pins.sort();
};

export default locationData;
