export const getBankByName = (name) => {
  return indianBanks.find(bank => 
    bank.name.toLowerCase() === name?.toLowerCase() || 
    bank.id === name?.toLowerCase()
  ) || indianBanks[indianBanks.length - 1]; // Return "Other Bank" as default
};

export const getBankLogo = (bankName) => {
  const bank = getBankByName(bankName);
  return bank ? bank.logo : '🏦';
};

export const getBankColor = (bankName) => {
  const bank = getBankByName(bankName);
  return bank ? bank.color : 'bg-gray-500';
};
